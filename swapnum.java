public class swapnum {
     public static void main(String[] args) {
        int temp,n1=30,n2=21;
        System.out.println("Before swapping the value of num1 is "+n1);
        System.out.println("Before swapping the value of num2 is "+n2);
        temp=n1;
        n1=n2;
        n2=temp;
        System.out.println(" After swapping the value of num1 is "+n1);
        System.out.println("After swapping the value of num2 is "+n2);
     }   
}
